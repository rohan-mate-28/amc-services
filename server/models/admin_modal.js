import mongoose from "mongoose";
const adminSchema=mongoose.Schema({
        
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
export const Admin=mongoose.model("Admin",adminSchema);